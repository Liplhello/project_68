public class MoreIfElseDemo {
    public static void main(String[] args) {
        int x = 7;
        if(x == 1){
            System.out.println("x的值为1!");
        }else if(x == 2){
            System.out.println("x的值为2!");
        }else if(x == 3){
            System.out.println("x的值为3!");
        }else{
            System.out.println("x不为1、2、3中的任意一个!");
        }
    }
}
