public class OperatorDemo03 {
    public static void main(String[] args) {
        int i = 10;
        int j = 3;
        System.out.println(i + " + " + j + " = " + (i + j));
        System.out.println(i + " - " + j + " = " + (i - j));
        System.out.println(i + " * " + j + " = " + (i * j));
        System.out.println(i + " / " + j + " = " + (i / j));
        System.out.println(i + " % " + j + " = " + (i % j));
    }
}
